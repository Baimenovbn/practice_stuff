let state = {
    'querySet': tableData,

    'page': 1,
    'rows': 5,
    'window': 5,
}

buildTable();

function pagination(querySet, rows, page) {
    let trimStart = (page - 1) * rows;
    let trimEnd = trimStart + rows;

    let trimmedData = querySet.slice(trimStart, trimEnd);
    let pages = Math.ceil(querySet.length / rows);
    return {
        querySet: trimmedData,
        pages,
    }
}

function pageButtons(pages) {
    let wrapper = document.querySelector('#pagination-wrapper');
    wrapper.innerHTML = '';
    let leftMax = Math.ceil(state.page - state.window / 2);
    let rightMax = Math.floor(state.page + state.window / 2);

    if (leftMax < 1) {
        leftMax = 1;
        rightMax = state.window;
    }

    if (rightMax > pages) {
        rightMax = pages;
        leftMax = pages - (state.window - 1);

        if (leftMax < 1) {
            leftMax = 1;
        }
    }

    for (let page = leftMax; page <= rightMax; page++) {
        let button = `<button value="${page}" class="page btn btn-sm btn-info">${page}</button>`;
        wrapper.insertAdjacentHTML('beforeend', button);
    }

    if (state.page >= 4) {
        wrapper.insertAdjacentHTML('afterbegin', `<button value="${1}" class="page btn btn-sm btn-info">&#171; First</button>`);
    }

    if (state.page < pages - state.window) {
        wrapper.insertAdjacentHTML('beforeend', `<button value="${pages}" class="page btn btn-sm btn-info">Last &#187;</button>`);
    }

    wrapper.addEventListener('click', function(e) {
        if (e.target.tagName === 'BUTTON') {
            document.querySelector('#table-body').innerHTML = '';
            state.page = parseInt(e.target.value);
            buildTable();
        }
    });
}


function buildTable() {
    let table = document.querySelector('#table-body');

    let data = pagination(state.querySet, state.rows, state.page);

    for (let person of data.querySet) {
        let row = `<tr>
                        <td>${person.rank}</td>
                        <td>${person.first_name}</td>
                        <td>${person.last_name}</td>
                    </tr>`;
        table.insertAdjacentHTML('beforeend', row);
    }
    pageButtons(data.pages);
}
